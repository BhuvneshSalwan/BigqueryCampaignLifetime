package com.google.cloud.bigquery.views;

public class DataView {

	private String hostname;
	private String ruleSmart;
	private String productSku;
	private String pagePath;
	private String productName;
	private String productCategoryLevel1;
	private String dimension1;
	
	public String getHostname() {
		return hostname;
	}
	public void setHostname(String hostname) {
		this.hostname = hostname;
	}
	public String getRuleSmart() {
		return ruleSmart;
	}
	public void setRuleSmart(String ruleSmart) {
		this.ruleSmart = ruleSmart;
	}
	public String getProductSku() {
		return productSku;
	}
	public void setProductSku(String productSku) {
		this.productSku = productSku;
	}
	public String getPagePath() {
		return pagePath;
	}
	public void setPagePath(String pagePath) {
		this.pagePath = pagePath;
	}
	public String getProductName() {
		return productName;
	}
	public void setProductName(String productName) {
		this.productName = productName;
	}
	public String getProductCategoryLevel1() {
		return productCategoryLevel1;
	}
	public void setProductCategoryLevel1(String productCategoryLevel1) {
		this.productCategoryLevel1 = productCategoryLevel1;
	}
	public String getDimension1() {
		return dimension1;
	}
	public void setDimension1(String dimension1) {
		this.dimension1 = dimension1;
	}
	
}
